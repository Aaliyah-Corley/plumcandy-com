  <h2>Plum Candy: The Rookie-Card Edition</h2>

  <ul>
    <li>
      Entirely printed and bound in Japan on luxurious Takeo “wet-touch” paper
    </li>
    <li>
      Smyth-sewn with metallic thread into a lay-flat, square-back monolith
    </li>
    <li>
      Hand-signed and hand-numbered by Aaliyah Corley while she was still 17
    </li>
    <li>
      Housed in a charcoal-linen clamshell box (the Sarcophagus), lined in black
      suede, with magnetic closure and grosgrain pull
    </li>
  </ul>

  <p>
    All made to feel like the Origin of the Myth, that it is, in your hands.
  </p>

  <p class="twist-text"><strong>But here’s the twist:</strong></p>

  <p>Not one dollar from this printing goes to the author or some publisher.</p>

  <p>
    It all goes directly into building the very culture that substantiates the
    legend.
  </p>

  <p>
    100,000+ free copies of a teacher’s edition mailed to every known
    high-school English teacher in America.
  </p>

  <p>
    Grants for the turnkey curriculum including the audiobook / ebook combo for
    all their students.
  </p>

  <p>Low cost copies mailed to everyone driving the pulse of culture.</p>

  <p>
    As the cultural significance of the novel grows, that increases its
    effectiveness as a teaching tool.
  </p>

  <p>
    The whole point is to make high school kids less dumb, while giving them a
    hero worth emulating.
  </p>

  <p>
    Normally, you dream of donating to a charity to have this type of impact.
  </p>

  <p>Now you get to fund the exact same charity —</p>

  <p>But with an outside shot at a power law payoff.</p>

  <p>
    Most literary first editions never appreciate; you should assume this one
    won’t either.
  </p>

  <p>
    If it doesn’t hit, you’re out the money you wanted to give away to help kids
    anyway. It’s STILL a great story you can tell your friends. “I knew it was a
    long shot, but we helped some kids and tried to give them a hero. I had to
    take a swing at that. It was a beautiful idea, and Plum Candy’s a great
    book, have you read it?”
  </p>

  <p>But if it becomes the next canon and you passed?</p>

  <p>Well, then you passed on Uber <em>and</em> you hate kids.</p>

  <p>Your name will be part of the legend either way.</p>

  <p>Villain or hero?</p>

  <p class="final-cta">Choose the legacy you want.</p>

