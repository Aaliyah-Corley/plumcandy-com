  <p class="intro">There is only one true rookie card in literature.</p>

  <p>
    The first printing of an author’s debut novel, made before anyone knows if
    it will matter.
  </p>

  <p>The Genesis.</p>

  <p>The Substrate of the Myth.</p>

  <p>
    That's the only true "first." The most collectible of artifacts — the Rookie
    Card.
  </p>

  <p>
    Every dollar of the multi-million (sometimes multi-billion) upside that
    eventually accrues to those artifacts is created years later by culture.
  </p>

  <p>
    A cheap first printing? It’s just “first”. It could be worth something, but
    it’s capped.
  </p>

  <p>
    A debut novel’s first run — the rookie card — should be a deliberately
    scarce, museum-grade art object.
  </p>

  <p>That uncaps the upside value on those artifacts.</p>

  <p>
    And it's an opportunity that has been squandered for every canon book in
    history.
  </p>

  <p>We are not squandering it.</p>

  <p class="highlight">Plum Candy is a debut novel by a 17-year-old unknown.</p>

  <p>10,000 copies.</p>

  <p>One printing.</p>

