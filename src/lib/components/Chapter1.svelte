    <h2>Preface</h2>

    <p>
        Is it really a betrayal if you should have seen it coming? You can’t
        think about that now.
    </p>

    <p>
        Darkness presses in, heavy, sapping — but not the kind of dark to hide
        in. You don’t see anyone, but are you alone? No.
    </p>

    <p>
        Your body knows before the mind. Hair stands up — a million nerve
        extenders seek out danger. The crawling sensation up your spine snaps a
        whip: <em>glance behind</em>.
    </p>

    <p>Freeze. Hold that noisy breath. Listen.</p>

    <p>
        You hear nothing. Nothing out of the ordinary. Not yet. How can it be so
        quiet and loud at the same time? It’s the heightened sense that both the
        hunted and the hunter share — that they’ve had to share since before
        time had a name. Pure survival.
    </p>

    <p>Before the fall it was all so simple.</p>

    <p>
        Now? Some people have their heart ripped out, others have their heart
        ripped out.
    </p>

    <p>
        That’s the tightening in your chest, the sinking sensation. The dreaded
        void.
    </p>

    <p>Will you run? Will you fight? Will you decide before it’s too late?</p>

    <p>All an illusion. There’s no real choice.</p>

    <p>It only feels like time is standing still. It is not.</p>

    <p>You’re gonna run. Go! GO!</p>

    <p>…</p>

    <p>All quiet now.</p>

    <hr />

    <h2>Chapter 1</h2>

    <p>
        Noelan Elyrite keeps his eyes fixed on the winding path ahead, this time
        the dirty road through Pine Ridge. Heading south, the morning sun warms
        his face. Noelan’s black hair doesn’t worry about a strong breeze from
        an open window, and neither does he.
    </p>

    <p>
        In the passenger seat, Lawson Wright works in Noelan’s shadow. His
        finger traces a paper map, “Should be just over those mountains. Pine
        Ridge. Quaint little town, if you believe the reports.”
    </p>

    <p>
        Noelan nodded, a casual one-handed grip on the steering wheel. “Small
        town, small problems? That it? Let’s see how long that keeps up. Guess
        we could use a plain Jane case for once.”
    </p>

    <p>
        “This straight shot looks a little dodgy,” Lawson tapped the map. “The
        highway will take a wee bit longer, roundabout as it is, but it’ll get
        us there without any headaches. Bear to the right at the fork up here.”
    </p>

    <p>Noelan nodded, grinned, and kept going straight.</p>

    <p>
        The car roared upward, climbing, revving, straining against that
        relentless slope, kept pushing, onward! it toiled and battled against
        cruel twists and dead-drop turns, onward!
    </p>

    <p>
        A sharp bend reshaped the view. Sunlight became a blade that stabbed
        through the windshield and punished Noelan’s eyes. He squinted. Motes of
        fractured color. His peripheral, blind. He had to focus or they’d fly
        off a cliff. Lawson was saying something, but it faded to nothing as
        Noelan focused round the dangerous turn.
    </p>

    <p>
        After the crest, the descent began. The shadow of the Ridge brought
        clarity. He started to see. There were trees, orange, yellow, red and
        evergreen pines and that small, insignificant-looking town. Those roads
        and cabins clung on to the east valley ridge. Continuing down, his
        vision cleared more.
    </p>

    <p>There was a lake, too.</p>

    <p>
        Noelan read aloud the words on a hand-carved wooden sign as the car
        whizzed by: “Pine Ridge, population 5,134.”
    </p>

    <p>
        “There,” Lawson pointed, “looks like a filling station. We should top up
        before heading into town.”
    </p>

    <p>
        Noelan rolled in beside a gas pump while the station’s bell rattled out
        like old bones. The clanging brought out a scrawny attendant with a
        grease-smudged forehead — couldn’t have been older than twenty.
    </p>

    <p>
        “I’ll handle this.” Noelan stepped out of the car. “Need to stretch my
        legs anyway.”
    </p>

    <p>The attendant nodded a greeting. “Good afternoon, sir. Fill ’er up?”</p>

    <p>
        The sign above the pump read ‘<em>REGULAR 27¢/GAL</em>’
    </p>

    <p>Noelan replied with a swagger, “Thanks kid. I’ll square up inside.”</p>

    <p>
        Gasoline pumping, price ticker spinning, Noelan made his way into the
        store. Above the door, another bell clanged.
    </p>

    <p>
        Inside: chrome counter, AM radio crackling through doo-wop, maps on a
        rack. Newly printed magazines vied for attention over a mess of locally
        made knickknacks and penny candies.
    </p>

    <p>
        On the wall behind the counter, a framed newspaper clip headlined: ‘<em
            >A PIECE OF FISHSTORY</em
        >’ with a picture of a man smiling while holding a particularly large
        trout. The caption read: ‘<em
            >Mayor Coolidge congratulates Ted Larson on record catch!</em
        >’ But someone had circled certain letters in red pen, then hastily
        attempted to erase the marks.
    </p>

    <p>
        Noelan noticed that the photographed local fishing legend was the
        shopkeeper behind the counter, “Howdy, stranger!”, who’d just greeted
        him.
    </p>

    <p>“Afternoon, Ted,” Noelan responded. “What do I owe you for the gas?”</p>

    <p>The shopkeeper answered as the store’s bell jingled again.</p>

    <p>
        A man stumbled in. The bandanna was red and paisley. <em>Paisley!</em> Like
        some dime-novel outlaw’s idea of a menacing mask. His clothes hung loose
        on his frame, perhaps “borrowed” from someone larger and too slow to catch
        him.
    </p>

    <p>
        His hand, though shaky, <em>did</em> brandish a revolver.
    </p>

    <p>“Hands up! Money in the bag, now!” the man yelled.</p>

    <p>
        The shopkeeper put his hands up quickly, then asked, “What bag? You got
        one for me or…”
    </p>

    <p>
        Noelan’s chin flicked upward sharp as a switchblade. Not worried, but
        amused. Amused by the robber’s dime-novel demeanor and kid-cowboy mask.
    </p>

    <p>He swung the gun towards Noelan. “I said hands up!”</p>

    <p>
        Swift but relaxed, Noelan stepped forward. He snatched the culprit’s
        wrist and threw him on the ground. The gun clattered pathetically to the
        floor as the robber yelped in pain, <em
            >“Aieee, ow, ow, ow! Gyaaah — mercy! Uncle!”</em
        >
    </p>

    <p>
        Before the man knew what happened, Noelan had him back on his feet,
        face-down on the counter, arm twisted behind his back. The whole
        kerfuffle lasted barely enough time to be a kerf.
    </p>

    <p>“Got any rope?” Noelan asked the gawking shopkeeper.</p>

    <p>“What?!? You gonna hang him?”</p>

    <p>“No. To tie him up. What kind of town is this?”</p>

    <p>
        “Small town. No rope. Got duct tape, though.” The shopkeeper reached
        under the counter and tossed a roll to Noelan.
    </p>

    <p>
        Noelan flipped the punk face-down across the counter, yanked his wrists
        together, and wrapped them tight with the silver tape (three quick
        loops, rip, done), then snagged the revolver off the floor, made sure it
        wasn’t cocked, and slipped it in his waistband.
    </p>

    <p>
        “Now then,” Noelan turned to the shopkeeper, his tone conversational,
        “how much for the gas, again?” He glanced at the bound man, “let’s add a
        bottle of pop for our unexpected guest.”
    </p>

    <p>“Duct tape, too?” grinned the shopkeeper.</p>

    <p>
        Noelan chuckled and paid the tab, then hauled the trussed-up robber out,
        Coke bottle dangling from his free hand like some cheap brown trophy.
    </p>

    <p>
        As Noelan opened the back door of the car and ushered the bound man
        inside, Lawson inquired, “Geez. Was that on sale?”
    </p>

    <p>“Small town. Already making new friends,” Noelan replied.</p>

    <p>
        He slid into the driver’s seat, started the engine, and moseyed away
        from the gas station, the crime scene, and two gobsmacked yokels.
    </p>

    <p>
        Back on the winding road, Lawson turned in his seat to eye their
        straggly captive, then back to his partner. “Noelan,” he said slowly,
        “We are here about the disappearances. Not to play beat cops.”
    </p>

    <p>Noelan didn’t reply.</p>

    <p>
        Lawson sighed and looked ahead. “We are headed to see Chief Travis
        anyway. I guess we could drop off some trash while we were there.”
    </p>

    <p>
        Noelan looked up to the rearview mirror and said, “So, care to explain
        yourself, friend?”
    </p>

    <p>
        The bound man shifted uncomfortably in his seat. “First off, my name’s
        not ‘friend,’ it’s Bennet,” he grumbled. Then, his voice dropping to a
        near-whisper, he added, “And I did it because I needed dough for the
        protection fee...”
    </p>

    <p>Lawson’s eyes narrowed. “Protection fee? What are you talking about?”</p>

    <p>
        Bennet looked out the window, his face reflective. “You’re new in town,
        ain’t ya? Don’t know how things work here in Pine Ridge.”
    </p>

    <p>
        Noelan side-eyed Lawson. This could be exactly the kind of information
        they had come to Pine Ridge to uncover.
    </p>

    <p>“Is it story time already?” Noelan asked.</p>

    <p>
        Bennet took a deep breath. “About two months ago the goons got more
        serious about the occasional light envelope. Before that it was more
        subtle. A little short just meant eatin’ a knuckle sandwich. But then,
        everyone got put on this strict weekly drop. And if you don’t pay...”
    </p>

    <p>He trailed off, his voice choked with emotion.</p>

    <p>“What happens if you can’t pay?” Noelan pressed.</p>

    <p>
        “Someone you love gets grabbed,” Bennet said, voice low and jagged.
        “Wife, kid, mom — they’re gone ‘til you pay up. And if you can’t get
        square? That special <em>someone</em> gets offed right in front of ’ya.”
    </p>

    <p>
        Silence slammed into the car. Noelan just reached up and adjusted the
        rearview mirror, a tiny click that framed Bennet’s miserable face in the
        glass. Then he settled back, one hand resting on the wheel like a stone.
        The engine hummed steady with the patience of a guillotine.
    </p>

    <p>
        “That’s why I stuck up the joint,” Bennet finally coughed up. “My
        fiancée... they said she looked too pretty for the gal of a broke mug
        like me. Promised to make the world right if I didn’t pony up by
        tonight. I couldn’t let that happen. I just couldn’t!”
    </p>

    <p>
        “Hmm,” was all Lawson Wright replied, a look of skepticism on his face.
    </p>

    <p>
        Noelan pulled the car into the long driveway when he found the sign and
        drove on until he saw it.
    </p>

    <p>
        The Pine Ridge Inn: two stories of raw pine stacked like a lumber
        baron’s after-hours binge.
    </p>

    <p>
        “I’ll wait here with our guest,” Lawson said. “You go book us a room.”
    </p>

    <p>
        Glaring coldly at Bennet in the backseat, Noelan said, “Alright. Stay
        put. Got it, knucklehead?”
    </p>

    <p>
        Noelan stepped out. The crunch of leaves under boots punctuated an eerie
        stillness that blanketed Pine Ridge. Rolling up, the Inn’s silhouette
        had loomed frontier-old, but up close, with the logs still bleeding sap,
        the sniff said otherwise.
    </p>

    <p>
        Inside felt warm and cozy: a fire going, wood polish, something floral
        like lilac... maybe. Behind a counter stood a man in his mid-forties,
        his friendly face creased with laugh lines that spoke of a life
        well-lived. Despite his genial appearance, Noelan noted the man’s broad
        shoulders and the hint of muscle beneath his rolled-up sleeves.
    </p>

    <p>
        “Welcome to Pine Ridge Inn.” His voice flowed. His eyes pierced. The
        chair creaked and groaned as the innkeeper rose. “What brings you to our
        neck of the woods?”
    </p>

    <p>
        Noelan leaned casually against the counter. “Exploring. Thought I’d get
        off the main road, and experience some of this countryside.”
    </p>

    <p>
        “Always good to broaden your horizons. You never know what nuggets of
        wisdom you might find... or what purpose you might fulfill.”
    </p>

    <p>Noelan sighed slightly. “What are you going on about?”</p>

    <p>
        The innkeeper chuckled and waved a hand dismissively. “Oh, don’t mind
        me. Small town. Big imagination. How long will you be staying?”
    </p>

    <p>“Six nights, if you have the room. Two beds, please.”</p>

    <p>
        While the innkeeper checked the ledger, Noelan took a gander around.
        Photos lined the walls, smiling families, local landmarks, a younger
        version of the innkeeper with his arm around a pretty woman.
    </p>

    <p>
        His gaze bounced from the photos to a glass jar on the counter, filled
        with small, individually wrapped, sweet, sweet candies. His eyes
        lingered, curiosity piqued.
    </p>

    <p>
        The innkeeper noticed Noelan’s interest and nodded. “Ah, you’ve spotted
        our little specialty.” He tapped the jar. “Plum candies — made right
        here in Pine Ridge. Go ahead, take as many as you like.”
    </p>

    <p>
        Noelan reached into the jar and pulled out a candy. He unwrapped and
        popped it into his mouth, enjoying the burst of flavor. “Sweet,” he
        remarked, almost to himself.
    </p>

    <p>
        Noelan smiled, letting the candy dissolve slowly as he looked around the
        cozy inn. He rolled it across his tongue. Then he really bit into it,
        crunching it into a mess of sharp shards in his mouth. “Though it’s got
        quite the bitter kick at the end.”
    </p>

    <p>
        The innkeeper chuckled. “Some say they’re the taste of the town. Keeps
        folks coming back.”
    </p>

    <p>
        Noelan kept his face stoic, but the taste was absurdly delicious —
        harsh, but strangely satisfying.
    </p>

    <p>
        “You’re in luck,” the innkeeper said, sliding a key across the counter.
        “Room 7, top of the stairs. Best view in the house. Enjoy your stay,
        Mr...?”
    </p>

    <p>“Noelan. Just Noelan.”</p>

    <p>
        “Enjoy your stay, Just Noelan,” the innkeeper said, “and if you need
        anything — advice, directions, a good cup of joe — you know where to
        find me.”
    </p>

    <p>
        Noelan took a fistful of candies, nodded, and strode out. Just as he
        left the inn, a soft weight on his foot gave him pause. Glancing down,
        he found himself under the scrutiny of a small cat. Its fluffy white and
        light gray fur gleamed in the sunlight. Bright blue eyes gazed up
        curiously at Noelan.
    </p>

    <p>
        Gently, Noelan scooped up the little feline. “I’m afraid I don’t have
        any food for you, little fellow,” he told the cat. The cat tilted its
        head, apparently confused by the interaction. He put it down, smiled,
        and gave it one final scratch on the head before leaving.
    </p>

    <p>
        Noelan walked back to the car and slid back into the driver’s seat, his
        expression neutral as he adjusted the rearview mirror. The failed robber
        shifted in the backseat; his bound hands denied any comfort.
    </p>

    <p>
        “So,” he ventured, voice tinged with desperation, “you’re not actually
        taking me to jail, right? Because then she’s good as gone.”
    </p>

    <p>
        Noelan chuckled and exchanged a glance with Lawson. “No jail. Not yet.”
    </p>

    <p>
        Relief softened Bennet’s face. Hands (still bound behind his back) rose
        hopeful, pathetic but hopeful. “Then... can I make tracks?”
    </p>

    <p>
        “Not so fast.” Lawson twisted in his seat to look back at him. “How
        exactly are you planning on getting that money?”
    </p>

    <p>
        Noelan didn’t say anything and simply glared at him through the rearview
        mirror.
    </p>

    <p>
        “Suppose I could get my piece back?” he muttered after a moment. “I’d
        still have time to—”
    </p>

    <p>
        “Wise up,” Lawson said. “You really think we’d let you go rob someone
        else?”
    </p>

    <p>Noelan scoffed. “Not a chance, jackass.”</p>

    <p>“But my fiancée!” Bennet’s voice cracked.</p>

    <p>
        Something cold crossed Noelan’s expression. “Is this fiancée of yours
        close by? Where’s she staying?”
    </p>

    <p>“Huh?” Bennet asked.</p>

    <p>
        “Where does your broad live?” Noelan said slowly, enunciating every
        word, like he was talking to a slow child, or a sputtering suspect.
        “We’ll head there now. Protect your little princess.”
    </p>

    <p>
        Bennet opened his mouth to spit out some excuse, but the words got
        somehow stuck when he caught Noelan’s glare. He slumped back in his
        seat, suddenly corporative.
    </p>

    <p>
        “Hang a left up past them elk, over there.” Despite the chill, sweat was
        beading Bennet’s brow.
    </p>

